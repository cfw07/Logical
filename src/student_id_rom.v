// ============================================================================
// 学号数字查找表 - 将位置索引映射为对应的学号数字
// Student ID Lookup Table - Maps position index to digit value
//
// 学号：20251698
// ┌───────┬───┬───┬───┬───┬───┬───┬───┬───┐
// │ 位置  │ 0 │ 1 │ 2 │ 3 │ 4 │ 5 │ 6 │ 7 │
// ├───────┼───┼───┼───┼───┼───┼───┼───┼───┤
// │ 数字  │ 2 │ 0 │ 2 │ 5 │ 1 │ 6 │ 9 │ 8 │
// └───────┴───┴───┴───┴───┴───┴───┴───┴───┘
//
// 学号含义：
//   20 - 入学年份(2020级)
//   25 - 学院/专业代码
//   1698 - 个人序号
// ============================================================================

module student_id_rom (
    input  wire  [2:0] addr,   // 数字位置索引（0~7）
    output reg   [3:0] digit   // 输出的BCD数字（0~9对应学号各位）
);

    always @(*) begin
        case (addr)
            3'd0: digit = 4'd2;  // 位置0：数字 2
            3'd1: digit = 4'd0;  // 位置1：数字 0
            3'd2: digit = 4'd2;  // 位置2：数字 2
            3'd3: digit = 4'd5;  // 位置3：数字 5
            3'd4: digit = 4'd1;  // 位置4：数字 1
            3'd5: digit = 4'd6;  // 位置5：数字 6
            3'd6: digit = 4'd9;  // 位置6：数字 9
            3'd7: digit = 4'd8;  // 位置7：数字 8
            default: digit = 4'd0;
        endcase
    end

endmodule
